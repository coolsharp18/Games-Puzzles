package happycube.pieces;

public class Corner {

	int positionX;
	int positionY;
	public int getPositionX() {
		return positionX;
	}

	public int getPositionY() {
		return positionY;
	}

	public Corner(int positionY, int positionX) {
		super();
		this.positionX = positionX;
		this.positionY = positionY;
	}

}
