package core.cube;

/**
 * Interface to a Cube. 
 * Marks the different faces of the cube as int constants.
 * 
 * LEFT = 0
 * TOP = 1
 * RIGHT = 2
 * BACK = 3
 * FRONT = 4
 * BOTTOM = 5
 * 
 * @author Anand Kumar
 *
 */

public interface Cube {
	
	int BOTTOM = 5;
	int TOP = 1;
	
	int FRONT = 4;
	int BACK = 3;
	
	int LEFT = 0;
	int RIGHT = 2;
}
